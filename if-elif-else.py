light = input("Enter the traffic light color (Red/Yellow/Green): ") 

if (light == "Red"):
    print("Stop")
elif (light == "Yellow"):
    print("Get Ready")
elif (light == "Green"):
    print("Move Now")
else:
    print("Invalid color")
